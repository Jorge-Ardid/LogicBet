def clean_team_name(name):
    parts = name.strip().split()
    half = len(parts) // 2
    if half > 0 and parts[:half] == parts[half:]:
        name = " ".join(parts[:half])
    else:
        words = []
        for w in parts:
            if w not in words:
                words.append(w)
        name = " ".join(words)
        
    for ukr, eng in UKR_TO_ENG.items():
        if ukr in name:
            return eng
    return name.strip()

def parse_and_insert():
    db = LogicBetDB()
    date_regex = re.compile(r"(\d+)\s+([а-яяіїє]+)\s+(\d{4})")
    match_regex = re.compile(r"(\d{1,2}:\d{2})\s+(.*?)\s*(\d+):(\d+)\s+(.*)")

    current_date_str = ""
    inserted = 0
    
    for line in RAW_DATA.split("\n"):
        line = line.strip()
        if not line or "Прем'єр-ліга" in line:
            continue
            
        date_match = date_regex.match(line)
        if date_match:
            day = date_match.group(1).zfill(2)
            month = MONTHS.get(date_match.group(2), "01")
            year = date_match.group(3)
            current_date_str = f"{year}-{month}-{day}"
            continue
            
        match_info = match_regex.match(line)
        if match_info:
            time_str = match_info.group(1)
            left_part = match_info.group(2)
            home_score = int(match_info.group(3))
            away_score = int(match_info.group(4))
            right_part = match_info.group(5)
            
            left_part = left_part.replace("Setanta Sports", "").strip()
            
            home_team = clean_team_name(left_part)
            away_team = clean_team_name(right_part)
            
            full_date = f"{current_date_str}T{time_str}:00+00:00"
            
            home_id = db.add_team_if_not_exists(home_team)
            away_id = db.add_team_if_not_exists(away_team)
            
            remote_id = -abs(hash(f"{home_team}_{away_team}_{current_date_str}")) % (10**8)
            
            db.insert_match(
                remote_id=remote_id,
                date=full_date,
                league="Premier League",
                league_id=39,
                home_id=home_id,
                away_id=away_id,
                h_score=home_score,
                a_score=away_score,
                status="FT"
            )
            print(f"Вставлено: {full_date} | {home_team} {home_score}:{away_score} {away_team}")
            inserted += 1

    print(f"\n✅ Успішно вставлено {inserted} матчів Астон Вілли в базу!")

if __name__ == "__main__":
    parse_and_insert()
