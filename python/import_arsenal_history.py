def clean_team_name(name):
    # Прибираємо дублі, наприклад "Арсенал Арсенал" -> "Арсенал"
    parts = name.strip().split()
    half = len(parts) // 2
    if half > 0 and parts[:half] == parts[half:]:
        name = " ".join(parts[:half])
    else:
        # Іноді може бути без дублів, або дубль з 1 слова
        words = []
        for w in parts:
            if w not in words:
                words.append(w)
        name = " ".join(words)
        
    for ukr, eng in UKR_TO_ENG.items():
        if ukr in name:
            return eng
    return name.strip()

def parse_and_insert():
    db = LogicBetDB()
    
    # Регулярки для парсингу дати і матчу
    # Формат дати: 17 серпня 2025
    date_regex = re.compile(r"(\d+)\s+([а-яяіїє]+)\s+(\d{4})")
    
    # Формат матчу: 18:30 Setanta Sports Манчестер Юнайтед Манчестер Юнайтед 0:1 Арсенал Арсенал
    # або: 19:30  Арсенал Арсенал 5:0 Лідс Юнайтед Лідс Юнайтед
    match_regex = re.compile(r"(\d{1,2}:\d{2})\s+(.*?)\s*(\d+):(\d+)\s+(.*)")

    current_date_str = ""
    inserted = 0
    
    for line in RAW_DATA.split("\n"):
        line = line.strip()
        if not line or "Прем'єр-ліга" in line:
            continue
            
        # Шукаємо дату
        date_match = date_regex.match(line)
        if date_match:
            day = date_match.group(1).zfill(2)
            month = MONTHS.get(date_match.group(2), "01")
            year = date_match.group(3)
            current_date_str = f"{year}-{month}-{day}"
            continue
            
        # Шукаємо матч
        match_info = match_regex.match(line)
        if match_info:
            time_str = match_info.group(1)
            left_part = match_info.group(2)
            home_score = int(match_info.group(3))
            away_score = int(match_info.group(4))
            right_part = match_info.group(5)
            
            # Прибираємо назви каналів типу "Setanta Sports" з лівої частини
            left_part = left_part.replace("Setanta Sports", "").strip()
            
            home_team = clean_team_name(left_part)
            away_team = clean_team_name(right_part)
            
            # Формуємо ISO дату
            full_date = f"{current_date_str}T{time_str}:00+00:00"
            
            home_id = db.add_team_if_not_exists(home_team)
            away_id = db.add_team_if_not_exists(away_team)
            
            # Вставляємо в базу (з фіктивним remote_id, наприклад негативним щоб не перетинатися з API)
            # Щоб remote_id був унікальним, згенеруємо його з хешу
            remote_id = -abs(hash(f"{home_team}_{away_team}_{current_date_str}")) % (10**8)
            
            db.insert_match(
                remote_id=remote_id,
                date=full_date,
                league="Premier League",
                league_id=39, # EPL
                home_id=home_id,
                away_id=away_id,
                h_score=home_score,
                a_score=away_score,
                status="FT"
            )
            print(f"Вставлено: {full_date} | {home_team} {home_score}:{away_score} {away_team}")
            inserted += 1

    print(f"\n✅ Успішно вставлено {inserted} матчів Арсеналу в базу!")
    print("Рекомендується запустити Elo Calculator, щоб оновити рейтинги цих команд на основі нових даних.")

if __name__ == "__main__":
    parse_and_insert()
